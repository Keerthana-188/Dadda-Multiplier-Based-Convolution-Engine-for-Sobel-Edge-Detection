`timescale 1ns / 1ps

module instruction_memory(
    input [31:0] pc,
    output [31:0] instruction_code
);

//////////////////////////////////////////////////////////
// MEMORY
//////////////////////////////////////////////////////////

reg [7:0] memory [0:200];

//////////////////////////////////////////////////////////
// INSTRUCTION FETCH
//////////////////////////////////////////////////////////

assign instruction_code = {
    memory[pc+3],
    memory[pc+2],
    memory[pc+1],
    memory[pc]
};

//////////////////////////////////////////////////////////
// INITIALIZATION
//////////////////////////////////////////////////////////

integer i;

initial
begin

    //////////////////////////////////////////////////////
    // CLEAR MEMORY
    //////////////////////////////////////////////////////

    for(i = 0; i < 200; i = i + 1)
        memory[i] = 8'h00;

memory[0] = 8'h83;
memory[1] = 8'h00;
memory[2] = 8'h20;
memory[3] = 8'h40;

memory[4] = 8'h13;
memory[5] = 8'h00;
memory[6] = 8'h00;
memory[7] = 8'h00;

memory[8]  = 8'h13;
memory[9]  = 8'h00;
memory[10] = 8'h00;
memory[11] = 8'h00;
end

endmodule